let currentLanguage = "en";

function t(key) {
  return data[currentLanguage][key];
}

function setLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language;
  document.title = language === "fi" ? "Oulun kliinisen hemodynamiikan tutkimusryhmä" : "Oulu Hemodynamics Research Group";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    element.textContent = t(key);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.setAttribute("placeholder", t(key));
  });
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });
  document.getElementById("footerTitle").textContent = `© ${t("heroTitle")}`;
  renderStats();
  renderThemes();
  renderApproach();
  renderMethods();
  renderPortfolio();
  renderResearchCollaborations();
  renderThemeFilter();
  renderProjects();
  renderCompleted();
  renderPublications();
}

